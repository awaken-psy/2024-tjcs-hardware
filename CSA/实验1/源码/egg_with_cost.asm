# 鸡蛋耐摔测试MIPS汇编程序
# 使用二分查找算法确定鸡蛋的耐摔临界楼层
# 同时计算不同经济时期的成本消耗

# 寄存器分配说明：
# $2: 建筑总楼层数
# $3: 鸡蛋耐摔阈值
# $4: 总测试次数计数器
# $5: 摔碎鸡蛋计数器
# $6: 最后测试状态标志 (1=摔碎, 0=完好)
# $7: 搜索下界
# $8: 搜索上界
# $9: 当前测试楼层
# $10: 常数1
# $11: 资源匮乏期成本
# $12: 人力成本增长期成本
# $13: 上行成本权重
# $14: 下行成本权重
# $15: 鸡蛋成本权重
# $16: 累计上行层数
# $17: 累计下行层数
# $18: 前次测试楼层
# $19: 临时寄存器1
# $20: 临时寄存器2
# $21: 临时寄存器3

.text
    # 初始化系统参数
    addi $2, $0, 128           # 设置建筑总楼层数 = 128
    addi $3, $0, 20            # 设置鸡蛋耐摔阈值 = 20
    addi $10, $0, 1            # 设置常数1
    
    # 初始化搜索边界
    add $7, $0, $0             # 搜索下界 l = 0
    add $8, $0, $2             # 搜索上界 r = 总楼层数
    
    # 初始化上次测试楼层为中间值
    addi $18, $2, 1            # 计算 (总楼层数 + 1)
    sra $18, $18, 1            # $18 = (总楼层数 + 1) / 2
    
    # 初始化计数器
    add $4, $0, $0             # 总测试次数 = 0
    add $5, $0, $0             # 摔碎鸡蛋数 = 0
    add $16, $0, $0            # 累计上行层数 = 0
    add $17, $0, $0            # 累计下行层数 = 0
    add $11, $0, $0            # 资源匮乏期成本 = 0
    add $12, $0, $0            # 人力成本增长期成本 = 0
    
    # 设置成本计算参数
    addi $13, $0, 2            # 上行成本权重 = 2
    addi $14, $0, 1            # 下行成本权重 = 1
    addi $15, $0, 4            # 鸡蛋成本权重 = 4

# 主测试循环
TEST_LOOP:
    # 检查循环条件: l < r
    slt $19, $7, $8            # 检查下界是否小于上界
    beq $19, $0, CALCULATE_COST # 如果 l >= r，跳转到成本计算
    
    # 增加测试次数计数
    addi $4, $4, 1             # 总测试次数加1
    
    # 计算当前测试楼层 (二分查找中点)
    add $9, $7, $8             # 计算 l + r
    addi $9, $9, 1             # 计算 l + r + 1
    sra $9, $9, 1              # mid = (l + r + 1) / 2
    
    # 计算楼层移动距离
    slt $19, $18, $9           # 比较前次楼层与当前楼层
    bne $19, $0, UPWARD_MOVE   # 如果前次楼层 < 当前楼层，上行
    
DOWNWARD_MOVE:
    # 下行移动处理
    sub $20, $18, $9           # 计算下行层数 = 前次楼层 - 当前楼层
    add $17, $17, $20          # 累加下行总层数
    j UPDATE_PREVIOUS_FLOOR
    
UPWARD_MOVE:
    # 上行移动处理
    sub $20, $9, $18           # 计算上行层数 = 当前楼层 - 前次楼层
    add $16, $16, $20          # 累加上行总层数

UPDATE_PREVIOUS_FLOOR:
    # 更新前次测试楼层记录
    add $18, $0, $9            # 前次楼层 = 当前楼层
    
    # 执行耐摔测试判断
    slt $19, $3, $9            # 比较耐摔阈值与当前楼层
    beq $19, $10, EGG_BROKEN   # 如果当前楼层 > 耐摔阈值，鸡蛋摔碎
    
EGG_INTACT:
    # 鸡蛋完好处理
    add $6, $0, $0             # 设置最后状态为完好 (0)
    add $7, $0, $9             # 更新搜索下界 = 当前楼层
    j TEST_LOOP
    
EGG_BROKEN:
    # 鸡蛋摔碎处理
    addi $5, $5, 1             # 摔碎鸡蛋计数加1
    add $6, $0, $10            # 设置最后状态为摔碎 (1)
    addi $8, $9, -1            # 更新搜索上界 = 当前楼层 - 1
    j TEST_LOOP

# 成本计算阶段
CALCULATE_COST:
    # 计算资源匮乏期成本: 2*上行 + 1*下行 + 4*摔碎数
    mul $19, $13, $16          # 计算 2 * 上行层数
    mul $20, $14, $17          # 计算 1 * 下行层数
    mul $21, $15, $5           # 计算 4 * 摔碎鸡蛋数
    add $11, $19, $20          # 累加前两项
    add $11, $11, $21          # 加上第三项 = 总成本
    
    # 计算人力成本增长期成本: 4*上行 + 1*下行 + 2*摔碎数
    addi $13, $0, 4            # 更新上行权重 = 4
    addi $15, $0, 2            # 更新鸡蛋权重 = 2
    mul $19, $13, $16          # 计算 4 * 上行层数
    mul $20, $14, $17          # 计算 1 * 下行层数
    mul $21, $15, $5           # 计算 2 * 摔碎鸡蛋数
    add $12, $19, $20          # 累加前两项
    add $12, $12, $21          # 加上第三项 = 总成本

# 程序结束
PROGRAM_END:
    j PROGRAM_END              # 无限循环，程序结束

.text ends